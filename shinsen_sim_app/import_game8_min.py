# 例として、行は大体こんなイメージになっている想定:
# 一領具足    S-001   アクティブ  敵軍複数に兵刃ダメージ ...
# 天下布武    S-002   アクティブ  敵軍全体に計略ダメージ ...

def normalize_skill_id(line: str) -> str | None:
    """
    1行の中から S-001 / S_001 / S 001 などを拾って "S_001" 形式にそろえる。
    """
    m = re.search(r"S[-_ ]?(\d{3})", line)
    if not m:
        return None
    num = m.group(1)
    return f"S_{num}"


def extract_name(line: str) -> str | None:
    """
    戦法名を推定する:
      行頭〜S-xxx の手前までを「戦法名」とみなす。
    """
    # 改行やタブをスペースにまとめる
    text = re.sub(r"\s+", " ", line).strip()
    m = re.search(r"(.+?)\s+S[-_ ]?\d{3}\b", text)
    if not m:
        return None
    name = m.group(1).strip()
    # 変な装飾を軽く削る（必要に応じて調整）
    name = name.replace("★5", "").replace("★4", "").strip()
    return name or None


def build_name_map(raw: str) -> Dict[str, str]:
    """
    RAW テキストから {skill_id: 戦法名} の辞書を作る。
    """
    mapping: Dict[str, str] = {}
    for raw_line in raw.splitlines():
        line = raw_line.strip()
        if not line:
            continue

        sid = normalize_skill_id(line)
        if not sid:
            continue

        name = extract_name(line)
        if not name:
            continue

        mapping[sid] = name

    return mapping


def main() -> None:
    if not SKILLS_PATH.exists():
        raise SystemExit(f"skills.json が見つかりません: {SKILLS_PATH}")

    skills = json.loads(SKILLS_PATH.read_text(encoding="utf-8"))
    if not isinstance(skills, list):
        raise SystemExit("skills.json の形式が配列ではありません。")

    name_map = build_name_map(RAW)
    if not name_map:
        raise SystemExit("RAW から戦法名が 1 件も抽出できませんでした。Game8 のテーブルをちゃんと貼れているか確認してください。")

    updated = 0
    for s in skills:
        sid = s.get("skill_id")
        if not sid:
            continue
        if sid in name_map:
            new_name = name_map[sid]
            s["name"] = new_name
            s["display_name"] = new_name
            updated += 1

    SKILLS_PATH.write_text(json.dumps(skills, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"skills.json を更新しました。{updated} 件の戦法名を上書きしました。")
    print("ファイル:", SKILLS_PATH)


if __name__ == "__main__":
    main()
